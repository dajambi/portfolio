                    <div class="rf_paragraph">
                        <pre><span>05</span> User-flow design</pre>
                        <h2>Create a custom list or&nbsp;select a template?</h2>
                        <p>
                            Initially, we had a vision that we will build a robust system that will cover also IT
                            project management. But we didn't get the sponsors for this kind of project size. Thus we
                            changed the scope and focus only for the task list.
                        </p>
                        <p>
                            Based on the observation I've come up with the idea of reusable task templates. It means
                            that the task list for a specific type of work will be created only once and then can reused
                            many times. Moreover experience colleagues can improve the template. So MSP would be able to
                            constantly improve the procedures and share their best practices.
                        </p>

