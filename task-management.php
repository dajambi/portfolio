                    <div class="section_name"><span>3 / 6</span> Development</div>

                    <div class="rf_paragraph">
                        <pre><span>04</span> Architecture design</pre>
                        <h2>Task templates</h2>
                        <p>
                            Initially, we had a vision that we will build a robust system that will cover also IT
                            project management. But we didn't get the sponsors for this kind of project size. Therefore
                            we changed the scope and focus only for the task list.
                        </p>
                        <p>Based on the observation I've come up with the idea of reusable task templates. It means that
                            the task list for a specific type of work will be created only once and then can be used
                            many times. Or the template could be improved by more experienced colleagues. So MSP
                            provider would be able to constantly improve the procedure, share the best practices and
                            improve the service quality.</p>

