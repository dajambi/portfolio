                    <div class="rf_paragraph">
                        <pre><span>01</span> My involvement & Challenge</pre>
                        <h2>What It was <span class="highlight_blue--light">about?</span></h2>
                        <p>
                        When I was working on the main Gordic web redesign, I've discovered via analytics a user needs to visit the support page. They wanted to get the law updates and they were actively seeking the best practices. When I highlighted this trend, the leaders of the company told me that they would like to solve this problem in the future. After some time the project manager called me and asked If I would help them design a new Support & learning port. We had a good relationship and I knew enough about the products and company. Naturally, I liked the idea that I can actually solve the problem that I've discovered.</p>

                        <ul>
                            <li>The Gordic had a highly educated team dedicated to establishing the process best-practices. Everything was is connected to law is quite complex. We had to structure the portal in a way that would represent the real world, but somehow simplify it to make it usable.</li>
                            <li>The second challenge was that the Visual design should be delivered by the external company and they had a delay. I had to find a way how to deliver it even though we were blocked by them.</li>
                        </ul>
                    </div>
                </section>


                <section class="rf_content">
                    <div class="section_name"><span>3 / 6</span> Discovery</div>
                    <div class="rf_paragraph">
                        <pre><span>02</span> Research</pre>
                        <h2>Identifying problems</h2>
                        <p>
                        Initially we had discussion what are the most frequent problems, what people often asks or what people don't understand on expert trainings, what is confusing on the legislative, where people are making the mistakes, what is causing the biggest pains in the industry. We gathered the data from the emails, feedback from trainings and expert knowledge of dedicated team. Based on that we together came up with the structure proposal.
                        </p>

                        <p>
                        I went through some existing web to find out how they present the information, and how the legislativ is actually structured and how the changes are presented to public. For this kind of project is important to adopt the know habits (Jakob‚Äôs Law) of the target audience. The bold changes could caused more issues than benefits. I've took the layout inspiration from other Gordic webs with similar content and from Czech and European union institution.
                        </p>

