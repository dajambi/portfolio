                    <div class="section_name"><span>4 / 6</span> WEB DESIGN</div>

                    <div class="rf_paragraph">
                        <pre><span>03</span> Planning & iterations</pre>
                        <h2>What will be the structure?</h2>
                        <p>
                        We didn't have a much time, I convinced the team to go in small-weekly iterations. This approached has proved it's worth in the previous project, so I've designed in the same way. I started with homepage to demonstrate the structure and then go the details. I've planned the review every week in the early morning before my normal job. First two iteration I've done in mid level fidelity.
                        Meanwhile the graphic studio came up with the visual proposal for the main Gordic web. Even though it wasn't perfect at that time, I've proposed the idea, that I will consume the visual style. The rest of the project I've done directly in high fidelity.
                        </p>
                    </div>

                    <div class="rf_paragraph">
                        <pre><span>04</span> Design iterations</pre>
                        <h2>Homepage</h2>
                        <p>
                        Based on the research I've come up with the information structure composed from four pillars ‚Äî Legislative, Methodic tutorials, Methodic for download and Best practices. There were three main usecases.
                        </p>
                        <ul>
                            <li>User will be looking for some specific law update, so they will use directly the search option(1).</li>
                            <li>User will come there to proactively check, whether there is something new in the legislation or other pillars(2).</li>
                            <li>User will come there to see what are the deadlines in this months and what needs to be done(3).</li>
                            <li>The last part was informing users about the legislation's most frequent questions which users actively asked the team(4).</li>
                        </ul>
                    </div>
                </section>
