                        <pre><span>09</span> Fifth increment</pre>
                        <h2>Online system configuration</span></h2>
                        <p>
                            The leaders had the vision to provide an online option for system configuration. In other
                            words, the Customer should have the ability to configure and buy a new system module online.
                            Similarly, as you can configure the car from your home. I used the lean approach and
                            designed the prototype that guided the user through the products. I've conducted guerrilla
                            testing with 3 people, who were working for the government and had experiences with similar
                            systems. And I tested it also with two other people from my network. </p>

                        <p>The outcome was, that people were able to go through the interactions and finish the process.
                            Also, they were quite confident that they finished the task. But the critical part was, <strong>that
                            participants needed to explicitly know what they should look for</strong>. And this mind model
                            doesn't correspond with real situations. Usually, the sales or implementation team works
                            closely with the customer to choose the correct module.
                            This idea was abandoned. In the future, there will be space to create a chatbot that will
                            guide customers through products or answer a customer questions.</p>

                        <p>Thanks to this exercise we discovered that the sales team need to find out the module based
                            on the search criteria. Because of that, the online catalog was developed later on.</p>
