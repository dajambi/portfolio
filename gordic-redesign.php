                    <div class="rf_paragraph">
                        <pre><span>02</span> Understand</pre>
                        <h2>Assumptions or data?</h2>
                        <p>
                            Brand Awareness issue Gordic people felt on daily basis in many domains. They hired Czech
                            marketing agency H1.cz to run a workshop where they analyzed the problems. This workshop had
                            happened before my involvement and the outcome of the workshop served as the starting point
                            for my ideation.
                        </p>

                        <p>
                            In the beginning, We lacked the data from the existing web. I quickly implemented at least
                            Google Analytics and Hotjar to gather quantitative data and user interactions. We collected
                            the data for three months and I spotted two trends. The majority of visitors visited two
                            pages — Expert Training and Support center. People were looking for support articles and
                            best practices, but it wasn't available on the web. This fact convinced the leadership to
                            start a new project.

                            <a href="gordic-support.php" target="_blank" class="link odkaz anim-middleout">I am describing it in this
                                case study.</a>

                            Also, these two findings had an impact on the new information architecture and navigation
                        </p>

                        <p>
                            I've done simple research, where I discovered how the competitors (SAP, Stormwater, Vera,
                            ICZ) communicate with the customer and how their product portfolio is structured. Why?
                            Because then you can find a space for improvements and you’ll also see what you can do
                            differently.
                        </p>
