                    <div class="rf_paragraph">
                        <pre><span>01</span> My involvement & Challange</pre>
                        <h2>What It was <span class="highlight_blue--light">about?</span></h2>
                        <p>
                            My friend who was in the Gordic security department ask me if I would be willing to help
                            them with the redesign. It sounded like a big challenge, so naturally, I had to say yes. I
                            mainly designed the wireframes, interactions, and user flows. But also later on I created
                            the visual design for emails and newsletters. Company was founded in 1993 as the software
                            company which developed information systems. But there were a few problems</p>

                        <ul>
                            <li>General brand awareness that Gordic is the accounting company </li>
                            <li>Gordic is a supplier of information systems for public institutions.</li>
                            <li>People's often perception, due to historic circumstances is, that company that is doing
                                something for the public sector is a also fraudster.</li>
                        </ul>
                        <strong>The main goals were</strong>
                        <ul>
                            <li>Change the brand awareness</li>
                            <li>Increase Leads generation</li>
                            <li>Direct upsell of IS modules (like e-shop)</li>
                            <li>Selling training courses</li>
                        </ul>
                    </div>
                </section>


