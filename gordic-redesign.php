                    <div class="rf_paragraph">
                        <pre><span>04</span> Design ideation & iterations</pre>
                        <h2>Designing the Core</h2>
                        <p>
                            Redesigning everything at once is always a bit dangerous and also kinda like a waterfall.
                            I've proposed to go further in small design weekly increments and create the quick concept.
                            Thanks to this lean approach, everyone had clear expectations. We've tested our
                            collaboration at the very beginning and I was able to deliver the design fast.
                        </p>

                        <p>
                            From the very beginning, I've focused on training courses. This had a huge revenue potential
                            and was one of the most visited pages. Part of that was also a concept for the product page
                            and homepage. After this main iteration, I've come up also with the concept for the system
                            configurator.
                            <br>
                            <br>
                        </p>
                    </div>

                    <div class="rf_paragraph">
                        <pre><span>05</span> First increment</pre>
                        <h2>Homepage</h2>
                        <p>
                            The homepage is usually about attention and interest. The first section should say that
                            Gordic is a software company. Hence there was displayed the main digital product with a
                            strong message. that was emphasized by The social proof emphasized all of that, plus it
                            supported the credibility. The next section was about the product portfolio. I wanted to
                            stress out, that they don't develop only accounting software.
                        </p>

                        <p>
                            One potential risk was a bad brand perception. Because of that, the following section was
                            about the company, milestones, and values. There the visitor should find out, that they are
                            responsible and very transparent with a long history.
                            The last part was about the upcoming events. Surprisingly this was very important for the
                            customer relationship.
                        </p>
                    </div>
                </section>
