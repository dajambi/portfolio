                    <div class="rf_paragraph">
                        <h2>What next?</h2>
                        <p>
                        It has begun the foundation for the digitalization of Customer support in Kiwi.com and since the initial release, we have done many improvements to increase the communication quality and eliminate the mistakes agent did. But most importantly we implemented the NLP technology so now we are able to measure the customer sentiment and adjust the communication or the whole communication strategy based on that. 
                        </p>
                        <p>We also know what are the real contact reasons and react accordingly to eliminate the root causes. We are able to show or filter only relevant information the support agent cares about. That means if you are solving customer requests related to the baggage then you gonna have a special environment just for this task and you gonna see the communication relevant just to this task. This focus has a very positive effect on the information cognitive load and in the end also on the request handling time and agent satisfaction.</p>

                        <p>Another improvement we've prepared is the ability to send an email right from the helpdesk. Agents have to communicate often with partners and other airlines so having everything in one place connected to the customer request makes the platform even more powerful and moreover, this eliminated mistakes caused by lost emails.</p>
