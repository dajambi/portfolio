                        <pre><span>05</span> Delivery of language-agnostic platform</pre>
                        <h2>Parlez-vous fran√ßais? Oui..., ¬øHablas espa√±ol? Tambi√©n...</h2>
                        <p>
                        After shipping the design, we have carefully watched one of the most important development and release we've done for dKiwi.com digitalization and we were finally able to talk with our customers again.ü§û 
                        </p>
                        <p>Wait, that is not all :-) So far we've relied on native speakers to translate customer inquiries. Our next priority was to build language-agnostic communication. We didn't have native-speaking agents during the lockdown who could handle the Japanese, Chinese, Portuguese and other languages. Finding, onboarding and training new native speakers would require six more months which we didn‚Äôt have.</p>

                        <p>So we've built the language-agnostic helpdesk platform together with <a href="http://unbabel.com/" rel="nofollow" target="_blank" class="link">Unbabel.com</a> that worked almost in real-time. How? Now customers can send us messages in their native language ‚Äî for example in Russian. This message is immediately translated to English for our agent by Unbabel's machine translation. Once our agent responds, the message goes to the machine translation again. But additionally, it is also double-checked by a human editor and delivered to the customer in a split dozen minutes. This ensures the native quality of the translation and keeps the cultural nuances.üá©üá™ üá®üá≥ üá∫üá∏ üá´üá∑</p>
