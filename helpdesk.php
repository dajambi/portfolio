                    <div class="rf_paragraph">
                        <pre><span>03</span> Ideation</pre>
                        <h2><span class="highlight_blue--light">How </span>we designed solution?</h2>
                        <p>
                        We are a product-led organization and our goal was to build an internal hybrid helpdesk platform to reduce the pressure on our support agents. With&nbsp;this solution in place we would be able to serve our customers again which means:
                        </p>
                        <ul>
                            <li>We would be able to reduce the strain on our phone is driven customer support system.</li>
                            <li>Prioritize communication-based on multiple aspects and customer situations.</li>
                            <li>Reduce the wait time from days to hours or minutes.</li>
                            <li>Mitigate the repetitive contact by showing the request status or related information.</li>
                            <li>Completely eliminate the spam and bring transparency to our long contact backlog.</li>
                        </ul>
                        <p>How have we done it? I've got a new message from one of Kiwi.com product managers ‚Äî "Hey, stop everything you are working on and check this RFC doc". All right, I've messaged my design colleague and we've immediately started quick design ideation. Back then, we were shifting tooling from Sketch to Figma, luckily. So We opened Figma and started jamming on the workflow.</p>
