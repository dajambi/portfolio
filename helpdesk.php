                    <div class="rf_paragraph">
                        <pre><span>01</span> Quick intro about Kiwi.com</pre>
                        <h2>Little bit of the <span class="highlight_blue--light">context</span></h2>
                        <p>
                        Kiwi.com is  a travel technology company and we are combining more than 800 different air and ground carriers into unique itineraries other search engines simply can‚Äôt see. So you can get from any A to any B in the world.
                        </p>
                        <p>We process 100 million searches daily, sell 40,000 seats per day (pre-pandemic), and have more than 200 active distribution partnerships. Supporting this amount of workflow interaction every day is quite challenging. For this reason, we has built an in-house helpdesk platform. You can imagine it as the Jira, but very customized to our use-cases.</p>
                        <p>Once the pandemic has struck the whole travel industry had to adapt to new increased volumes of customers‚Äô contacts.¬† I would like to tell you how we've changed our product & customer support experience <b>during one of the biggest travel crises of the modern era.</b></p>
                        <p>Without any longer ado, let's jump ü¶òinto the details and see how the design team played a key role in the digital transformation.</p>
                    </div>

                    <div class="rf_paragraph">
                        <pre><span>02</span> Challenge</pre>
                        <h2><span class="highlight_blue--light">What </span>would you do if... </h2>
                        <p>
                        In the normal situation if something went wrong, or if you needed some support, you simply sent the email to Kiwi.com or called the customer support team. This system worked. We had several contact centers around the world. The service was reliable no matter your current location or time. There was always someone available and ready to help.
                        </p>
                        <p>But in early <b>2020 the crisis suddenly came</b> and the majority of our contact centers, including centers in the Philippines and India, got closed.</p>
                        <p><b>What would you do if you had 7 times more contacts, but only 30% of the customer support workforce would be available because of the lockdown?</b> How would you deal with thousands of emails every day with no clue whether it is spam or an urgent customer request? How would you help your customers if you knew that your hotline was busy, because of thousands of repetitive contacts caused by the 80% flight cancellation rate at the beginning of the pandemic?</p>
                    </div>
                </section>

