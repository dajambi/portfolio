                    <div class="rf_paragraph">
                        <h2>Connecting the customer</h2>
                        <p>
                        With the team we've decided to simplify the flow to <b>focus on the core&nbsp;‚Äî&nbsp;send messages & respond to the customer.</b> Then we immediately started design the interface. In the meantime, the development team started the Front-end and Back-end preparation to speed up the delivery. 
                        </p>
                        <p>What was the flow? In the nutshell, when a customer got a problem, he /she went to the mobile or web app and open the detail of the trip where was the option to send us a message. Then the system prioritizes the queue based on multiple factors. Then the Customer support agent reviews the customer request. If the agent needed more information he simply sends the question to the customer or just send a reply, that everything was solved and then he changed the status either to done or to waiting on a customer.</p>
